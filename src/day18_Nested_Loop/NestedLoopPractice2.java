package day18_Nested_Loop;

public class NestedLoopPractice2 {
    public static void main(String[] args) {

        for (int j = 0; j < 10; j++) {
            for (int i = 0; i <10 ; i++) {
                System.out.print("* ");
            }
            System.out.println();

        }



    }
}

