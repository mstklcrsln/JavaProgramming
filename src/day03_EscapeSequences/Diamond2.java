package day03_EscapeSequences;

public class Diamond2 {
    public static void main(String[] args) {

        System.out.println("\t\t\t           ^");
        System.out.println(" \t\t\t         /  \\");
        System.out.println(" \t\t\t       /   *   \\");
        System.out.println("\t\t\t      /  *   *    \\");
        System.out.println("\t\t\t    /   *    *   *   \\");
        System.out.println(" \t\t\t    -------------");
        System.out.println(" \t\t\t   \\  *    *      *  /");
        System.out.println("\t\t\t    \\    *    *    /");
        System.out.println("\t\t\t     \\     *    /");
        System.out.println("\t\t\t       \\     /");
        System.out.println(" \t\t\t         v");

    }
}
